package org.iottree.core.ext;

/**
 * Extends Prop definition item
 * 
 * @author jason.zhu
 */
public class AttrItem
{
	String name = null ;
	
	String title = null ;
	
	String desc = null ;
	
	public AttrItem()
	{}
	
	public String getName()
	{
		return this.name ;
	}
	
	public String getTitle()
	{
		return this.title ;
	}
	
	public String getDesc()
	{
		return this.desc ;
	}
}
